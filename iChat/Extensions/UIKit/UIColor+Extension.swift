import Foundation
import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor{
        return #colorLiteral(red: 1, green: 0.3328256602, blue: 0.1925725571, alpha: 1)
    }
    
    static func mainWhite() -> UIColor{
        return #colorLiteral(red: 1, green: 0.9837339024, blue: 0.9677769679, alpha: 1)
    }
    
    static func buttonBlack() -> UIColor{
        return #colorLiteral(red: 0, green: 0, blue: 0, alpha: 1)
    }
    
    static func textFieldLight() -> UIColor{
        return #colorLiteral(red: 0.6000000238, green: 0.6000000238, blue: 0.6000000238, alpha: 1)
    }
}
